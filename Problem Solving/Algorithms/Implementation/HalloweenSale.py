# Complete the howManyGames function below.
def howManyGames(p, d, m, s):
    count=-1
    while s>=0:
        s-=p
        count+=1
        if p>d and p-d>m:
            p-=d
        else:
            p=m
    print(count) 
    return count   
if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    pdms = input().split()

    p = int(pdms[0])

    d = int(pdms[1])

    m = int(pdms[2])

    s = int(pdms[3])

    answer = howManyGames(p, d, m, s)

    fptr.write(str(answer) + '\n')

    fptr.close()
